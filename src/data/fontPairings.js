// Fungsi untuk mendapatkan semua mood yang tersedia
export const getAvailableMoods = () => {
  const moods = [...new Set(fontPairings.map(pairing => pairing.mood))];
  return moods.sort();
};

// Fungsi untuk mendapatkan pairing berdasarkan mood
export const getPairingsByMood = (mood) => {
  if (mood === 'all') return fontPairings;
  return fontPairings.filter(pairing => pairing.mood === mood);
};

// Fungsi untuk mencari pairing berdasarkan nama font
export const searchPairings = (query) => {
  const searchTerm = query.toLowerCase();
  return fontPairings.filter(pairing => 
    pairing.headingFont.toLowerCase().includes(searchTerm) ||
    pairing.bodyFont.toLowerCase().includes(searchTerm) ||
    pairing.description.toLowerCase().includes(searchTerm) ||
    pairing.bestFor.toLowerCase().includes(searchTerm)
  );
};

// Statistik
export const getStats = () => {
  const moods = getAvailableMoods();
  const stats = {};
  
  moods.forEach(mood => {
    stats[mood] = fontPairings.filter(p => p.mood === mood).length;
  });
  
  return {
    total: fontPairings.length,
    byMood: stats
  };
};